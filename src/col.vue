<template>
  <div
    class="col"
    :class="colClass"
    :style="colStyle"
  >
    <slot></slot>
  </div>
</template>
<script>
let validator = value => {
  let keys = Object.keys(value);
  let valid = true;
  keys.forEach(key => {
    if (!["span", "offest"].includes(key)) {
      valid = false;
    }
  });
  return valid;
};
export default {
  name: "lunzi-col",
  props: {
    span: {
      type: [Number, String]
    },
    offest: {
      type: [Number, String]
    },
    ipad: { type: Object, validator },
    narrowPc: { type: Object, validator },
    pc: { type: Object, validator },
    widePc: { type: Object, validator }
  },
  data() {
    return {
      gutter: 0
    };
  },
  methods: {
    creteClasses(obj, str = "") {
      if (!obj) {
        return [];
      }
      let array = [];
      if (obj.span) {
        array.push(`col-${str}${obj.span}`);
      }
      if (obj.offest) {
        array.push(`col-${str}${obj.offest}`);
      }
      return array;
    }
  },
  computed: {
    colClass() {
      let { span, offest, ipad, narrowPc, pc, widePc } = this;
      let creteClasses = this.creteClasses;
      return [
        ...creteClasses({ span, offest }),
        ...creteClasses(ipad, "ipad-"),
        ...creteClasses(narrowPc, "narrow-pc-"),
        ...creteClasses(pc, "pc-"),
        ...creteClasses(widePc, "wide-pc-")
      ];
    },
    colStyle() {
      return {
        paddingLeft: this.gutter / 2 + "px",
        paddingRight: this.gutter / 2 + "px"
      };
    }
  }
};
</script>
